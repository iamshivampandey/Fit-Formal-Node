// Signup API endpoint with reusable validation (with password)
router.post('/signup', validationMiddleware.validateUserRegistration, async (req, res) => {
  try {
    console.log('üîÑ Signup request received:', req.body);
    const { email, password, firstName, lastName, phoneNumber, roleName } = req.body;

    // Default to 'Customer' if roleName is not provided
    const roleNameToUse = roleName || 'Customer';
    console.log('üîÑ Role to assign:', roleNameToUse);

    // Get role by name
    console.log('üîÑ Looking up role:', roleNameToUse);
    let role;
    try {
      role = await databaseService.db.GetRoleByName(roleNameToUse);
      if (!role) {
        console.log('‚ùå Invalid role name:', roleNameToUse);
        return res.status(400).json({
          success: false,
          message: `Invalid role name: ${roleNameToUse}. Valid roles are: Admin, Customer, Seller, Tailor, Taylorseller`
        });
      }
      console.log('‚úÖ Role found:', role);
    } catch (error) {
      console.error('‚ùå Error fetching role:', error);
      return res.status(500).json({
        success: false,
        message: 'Failed to fetch role information',
        error: process.env.NODE_ENV === 'development' ? (error.message || error.toString()) : undefined
      });
    }

    // Check if user already exists in database
    try {
      console.log('üîÑ Checking if user already exists in database...');
      const existingUser = await databaseService.db.GetUserByEmail({ Email: email });
      if (existingUser && Array.isArray(existingUser) && existingUser.length > 0) {
        console.log('‚ùå User already exists:', email);
        return res.status(409).json({
          success: false,
          message: 'User with this email already exists'
        });
      }
      console.log('‚úÖ User does not exist, proceeding with signup');
    } catch (error) {
      console.log('‚ö†Ô∏è Error checking existing user:', error);
      // Continue with signup if we can't check (database might be down)
    }

    // Hash the password
    console.log('üîÑ Hashing password...');
    const saltRounds = 12;
    const hashedPassword = await bcrypt.hash(password, saltRounds);
    console.log('‚úÖ Password hashed successfully');

    // Create new user object (with password and without id - will be auto-generated by database)
    const newUser = {
      email,
      password: hashedPassword,
      firstName,
      lastName,
      phoneNumber: phoneNumber || null,
      createdAt: new Date().toISOString(),
      modifiedAt: new Date().toISOString(),
      isActive: true
    };

    // Save user to database using database service
    let userId;
    try {
      console.log('üîÑ Saving user to database using database service...');
      const result = await databaseService.db.InsertUser(newUser);
      console.log('‚úÖ User saved to database successfully');
      console.log('üìä Database result:', result);
      
      // Get the inserted user ID from the result
      if (result && result.recordset && result.recordset.length > 0) {
        userId = result.recordset[0].id;
        console.log('‚úÖ User ID retrieved:', userId);
      } else {
        throw new Error('Failed to retrieve user ID after insertion');
      }
    } catch (error) {
      console.error('‚ùå Error saving user to database:', error);
      return res.status(500).json({
        success: false,
        message: 'Failed to save user to database',
        error: process.env.NODE_ENV === 'development' ? (error.message || error.toString()) : undefined
      });
    }

    // Insert user-role mapping
    try {
      console.log('üîÑ Assigning role to user...');
      await databaseService.db.InsertUserRole(userId, role.id);
      console.log('‚úÖ Role assigned successfully');
    } catch (error) {
      console.error('‚ùå Error assigning role to user:', error);
      return res.status(500).json({
        success: false,
        message: 'User created but failed to assign role',
        error: process.env.NODE_ENV === 'development' ? (error.message || error.toString()) : undefined
      });
    }

    // Remove password from response
    const { password: _, ...userResponse } = newUser;

    console.log('üéâ User created successfully:', userResponse.email);
    res.status(201).json({
      success: true,
      message: 'User created successfully',
      data: {
        user: userResponse,
        role: {
          id: role.id,
          name: role.role_name,
          description: role.description
        }
      }
    });

  } catch (error) {
    console.error('‚ùå Signup error:', error);
    res.status(500).json({
      success: false,
      message: 'Internal server error during signup',
      error: process.env.NODE_ENV === 'development' ? error.message : undefined
    });
  }
});

