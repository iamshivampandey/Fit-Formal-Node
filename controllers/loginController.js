// Login API endpoint with JWT token generation
router.post('/login', validationMiddleware.validateUserLogin, async (req, res) => {
  try {
    console.log('üîÑ Login request received:', req.body.email);
    const { email, password } = req.body;

    // Find user by email in database
    console.log('üîÑ Searching for user in database...');
    const users = await databaseService.db.GetUserByEmail({ Email: email });
    if (!users || users.length === 0) {
      console.log('‚ùå User not found:', email);
      return res.status(401).json({
        success: false,
        message: 'Invalid email or password'
      });
    }

    const user = users[0];
    console.log('‚úÖ User found in database');

    // Check if user is active
    if (!user.isActive) {
      console.log('‚ùå User account deactivated:', email);
      return res.status(401).json({
        success: false,
        message: 'Account is deactivated'
      });
    }

    // Verify password
    console.log('üîÑ Verifying password...');
    console.log('üìã Password from request:', password ? 'provided' : 'missing');
    console.log('üìã Hashed password from DB:', user.password ? 'exists' : 'missing');
    
    if (!password || !user.password) {
      console.log('‚ùå Missing password data');
      return res.status(401).json({
        success: false,
        message: 'Invalid email or password'
      });
    }

    const isPasswordValid = await bcrypt.compare(password, user.password);
    if (!isPasswordValid) {
      console.log('‚ùå Invalid password for user:', email);
      return res.status(401).json({
        success: false,
        message: 'Invalid email or password'
      });
    }

    // Generate JWT token
    console.log('üîÑ Generating JWT token...');
    const tokenPayload = {
      userId: user.id,
      email: user.email,
      firstName: user.firstName,
      lastName: user.lastName
    };

    const token = jwt.sign(
      tokenPayload,
      process.env.JWT_SECRET,
      { 
        expiresIn: '24h', // Token expires in 24 hours
        issuer: 'your-app-name',
        audience: 'your-app-users'
      }
    );

    console.log('‚úÖ JWT token generated successfully');

    // Remove password from response
    const { password: _, ...userResponse } = user;

    console.log('‚úÖ Login successful for user:', email);
    res.json({
      success: true,
      message: 'Login successful',
      data: {
        user: userResponse,
        token: token,
        expiresIn: '24h'
      }
    });

  } catch (error) {
    console.error('‚ùå Login error:', error);
    res.status(500).json({
      success: false,
      message: 'Internal server error during login',
      error: process.env.NODE_ENV === 'development' ? (error.message || error.toString()) : undefined
    });
  }
});

